	CREATE DATABASE CustomerOrdersDB;
	USE CustomerOrdersDB;

-- 2) Create a table to store order details with columns for order ID, customer name, product name, quantity, price, and order date.
	CREATE TABLE orders (
    order_id INT PRIMARY KEY AUTO_INCREMENT,
    customer_name VARCHAR(100),
    product_name VARCHAR(100),
    quantity INT,
    price DECIMAL(10,2),
    order_date DATE
);

